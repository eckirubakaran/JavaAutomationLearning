public class countTheWords {

	public static void main(String[] args) {
		
		String sen="Count Words in a String";
		int count=0;
		

//		for(int i=sen.length()-1;i>=0;i--) 
		
		for (int i=0;i<sen.length();i++){
			
			sen.charAt(i);
			count++;
				
		}
		System.out.println(count);

	}

}
